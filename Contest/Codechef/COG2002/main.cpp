const int MAX = 1e5;
int a[MAX];
int n, t;
int res;

void solve() {
	cin >> t;
	while (t--) {
		cin >> n;
		 res = 0;
		for (int i = 0; i < n; i++) {
			cin >> a[i];
		}
		for (int i = 0; i < n; i++) {
			a[n + i] = a[i];
		}
		for (int i = 2; i < 2 * n - 2; i++) {
			int adjacents = max(a[i - 2] + a[i - 1], a[i + 1] + a[i + 2]);
			int value = a[i] + adjacents;
			res = max(res, value);
		}
		cout << res << endl;
	}
}



